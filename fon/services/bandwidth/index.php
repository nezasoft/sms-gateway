									<h4>Dedicated Bandwidth</h4>
									<p>Dedicated bandwidth is bandwidth that's reserved solely for the use of one subscriber, usually a business</p>
									<p>The ring-fencing of the bandwidth ensures that the business always gets the same high data throughput, regardless of what other subscribers may do with their connections. Unlike broadband connections, there's no drop in data throughput at peak times.</p>
									<p>Dedicated bandwidth is usually symmetrical. This means that the connection can send and receive data at the same high speed.</p>
									<p>Such connections offer more than just a high & predictable rate of data throughout. They tend to provide low delays in data transmission (latency) and low variations in that delay (jitter). The cost of a dedicated connection depends on the location being connected and the amount of bandwidth require</p>
									
									<h4>So why go for dedicated bandwidth?</h4>
									
									<p>Typically, it will be used to carry one or more of the following types of traffic: </p>									
									<li>Internet traffic - Web pages, emails, files being transferred by FTP, VPN traffic from staff working from home.</li>
                                    <li>Corporate data travelling between offices of the same company - Internal emails, spreadsheets, word documents, traffic generated by internal applications such as CRM systems.</li>
                                    <li>Telephone calls - Just as your music can be digitised, so can your telephone calls.  This allows them to be sent over your data network.</li>
                                    <li>Video Conferencing - This is often used by organisations with multiple offices to cut the amount of time staff have to waste travelling to meetings.</li>
                                 <p>
Data Backup & Server Replication - Most businesses would be in serious trouble if they lost their email server, their database server, their file servers or their internally-hosted applications. It's important to back up these business-critical systems frequently so that services can be restored if something goes wrong.</p> <p> As restoring an entire server can take many hours, it may be wise to replicate some critical servers, so that if there's a serious hardware failure, the users can access an almost identical copy of the lost server, while the primary server is being fixed. A high-capacity dedicated connection can provide the bandwidth required to backup & restore data, and to replicate servers.</p>
									
									
