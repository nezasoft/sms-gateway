									<h4>Local Loop Solutions</h4>
									
									<p>
									In telephony, a local loop is the wired connection from a telephone company's central office in a locality to its customers' telephones at homes and businesses. This connection is usually on a pair of copper wires called twisted pair.</p>
<p>									The system was originally designed for voice transmission only using analog transmission technology on a single voice channel. Today, your computer's modem makes the conversion between analog signals and digital signals.</p> <p> With Integrated Services Digital Network (ISDN) or Digital Subscriber Line (DSL), the local loop can carry digital signals directly and at a much higher bandwidth than they do for voice only.</p>
									</p>
									<p>FON offers local loop solutions based on fibre in the last mile. Whatever you want to transmit, FON local loop solutions are ideal for high bandwidth applications such as television signal distribution, video on demand, telephony, high speed internet access, digital television distribution, inter branch connectivity.</p>
                                     
								
									
		
